msg := 'Aggiornati CU spostandoli su '|| org_id_max ||' e cancellate ' || count_org_duplicati-1 || ' organization';

ELSE

msg := 'Errore max org_id';

	END IF;

ELSE

msg := 'Errore mancanza duplicati';
	
END IF;





	return msg ;
 END;
$BODY$
